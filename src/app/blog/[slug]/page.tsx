export async function generateMetadata({ params }: { params: { slug: string } }): Promise<Metadata> {
  const post = blogPosts[params.slug];
  
  if (!post) {
    return {
      title: 'Post Not Found - Digital Sheakh Blog',
    };
  }

  return {
    title: `${post.title} - Digital Sheakh Blog`,
    description: post.excerpt,
    keywords: `${post.category}, Digital Sheakh, ${post.title}`,
  };
}

